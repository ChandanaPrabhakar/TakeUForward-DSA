package LogicalThinkingPatterns.NumberedRectanglePattern;

import java.util.Scanner;

public class NumberedRectangle {
    private static void printPattern(int n) {
        for (int i = 0; i < 2 * n - 1; i++) {
            for (int j = 0; j < 2 * n - 1; j++) {
                int top = i;
                int left = j;
                int bottom = (2 * n - 2) - i;
                int right = (2 * n - 2) - j;
                System.out.print(n - Math.min(Math.min(bottom, top), Math.min(right, left)));
            }
            System.out.println();
        }
    }

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        printPattern(n);
        in.close();
    }
}
